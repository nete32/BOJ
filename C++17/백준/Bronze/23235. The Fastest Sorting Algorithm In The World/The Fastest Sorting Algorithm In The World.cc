#include <iostream>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int i = 1;
    while (true) {
        int N; cin >> N;
        if (N == 0)
            break;

        while (N--) {
            int X; cin >> X;
        }
        cout << "Case " << i << ": Sorting... done!\n";
        i++;
    }
    return 0;
}